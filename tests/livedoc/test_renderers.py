def test_test_inventory_table_renders():
    facts = {
        "total": 100,
        "groups": [
            {"tree": "kernel", "count": 80, "files": 5},
            {"tree": "plugins/links/tcp", "count": 20, "files": 2},
        ],
    }
    out = renderers.test_inventory_table(facts)
    assert "100 test cases across 2 tree(s)" in out
    # Kernel group hyperlinks to tests/.
    assert "[`tests/`](../../tests)" in out
    assert "plugins/links/tcp" in out
    # Per-row case counts in the cells.
    assert "| 80 |" in out
    assert "| 20 |" in out


def test_test_inventory_table_empty():
    out = renderers.test_inventory_table({"groups": []})
    assert "No test trees" in out
