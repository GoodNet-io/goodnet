"""Unit tests for tools/livedoc/renderers.py."""

from __future__ import annotations

from livedoc import renderers


# ── ABI renderers ───────────────────────────────────────────────────

_HOST_API_FACTS = {
    "struct": "host_api_s",
    "header": "sdk/host_api.h",
    "named_slots": 3,
    "reserved_slots": 2,
    "families": {
        "Messaging":       ["send", "disconnect"],
        "Registry queries": ["find_conn_by_pk"],
    },
    "slots": [
        {"name": "send", "signature": "int (*)()",
         "file": "sdk/host_api.h", "line": 10, "family": "Messaging",
         "doc": "Send envelope."},
        {"name": "disconnect", "signature": "int (*)()",
         "file": "sdk/host_api.h", "line": 14, "family": "Messaging",
         "doc": "Close."},
        {"name": "find_conn_by_pk", "signature": "int (*)()",
         "file": "sdk/host_api.h", "line": 18,
         "family": "Registry queries", "doc": "Look up."},
    ],
}


def test_host_api_summary_lists_every_family():
    out = renderers.host_api_summary(_HOST_API_FACTS)
    assert "3 named slots" in out
    # Reserved count surfaces as `2` (rendered with backticks).
    assert "`2` reserved" in out
    assert "Messaging" in out
    assert "Registry queries" in out
    assert "`send`" in out


def test_host_api_summary_carries_generated_note():
    assert "generated by tools/livedoc.py" in renderers.host_api_summary(
        _HOST_API_FACTS,
    )


def test_host_api_slots_table_shape():
    out = renderers.host_api_slots(_HOST_API_FACTS)
    assert "| Slot | Signature | Family |" in out
    # Three slot rows.
    rows = [ln for ln in out.splitlines() if ln.startswith("| [")]
    assert len(rows) == 3


def test_vtable_slots_pipe_escape():
    facts = {
        "struct": "x_t", "header": "sdk/x.h",
        "named_slots": 1, "reserved_slots": 0,
        "slots": [{
            "name": "weird", "signature": "int|fn|()",
            "file": "sdk/x.h", "line": 1, "family": "f", "doc": "",
        }],
    }
    out = renderers.vtable_slots(facts)
    # Pipe characters must be escaped or stripped so the GFM table
    # parser doesn't choke.
    bad_row = [ln for ln in out.splitlines()
               if ln.startswith("|") and "weird" in ln][0]
    assert bad_row.count("|") == 4 or "int\\|fn\\|" in bad_row


# ── Roadmap renderer ────────────────────────────────────────────────

def test_roadmap_status_table_badges():
    facts = {"features": [
        {"name": "A", "status": "done", "evidence": "ev1"},
        {"name": "B", "status": "partial", "evidence": "ev2"},
        {"name": "C", "status": "missing", "evidence": "ev3"},
    ]}
    out = renderers.roadmap_status_table(facts)
    assert "✓ done" in out
    assert "🚧 partial" in out
    assert "✗ missing" in out
    # Evidence column populated.
    for ev in ("ev1", "ev2", "ev3"):
        assert ev in out


def test_roadmap_status_table_empty():
    out = renderers.roadmap_status_table({"features": []})
    assert "Feature" in out


